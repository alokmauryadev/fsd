function printSequence(length) {
  // First, build the string sequence based on the given length.
  let sequence = '';
  for (let i = 1; i <= length; i++) {
    sequence += i;
  }
console.log(sequence);
}

// Example usage:
printSequence(6); // This will print "123456" six times.

